void lturn(int angle){
	while(SensorValue[right]<(angle*0.5)){
		motor[right1] = 40;
		motor[right2] = 40;
		motor[left1] = 40* correction ;
		motor[left2] = 40* correction ;
	}
	pause();
}
void rturn(int angle){
	//while(SensorValue[right]>-(angle*0.5)){
	while(SensorValue[left]<(angle*0.5)){
		motor[right1] = -40;
		motor[right2] = -40;
		motor[left1] = -40* correction ;
		motor[left] = -40* correction ;
	}
	pause();
	
}

