void pause(){
	motor[right1] = 0;
	motor[right2] = 0;
	motor[left1] = 0;
	motor[left2] = 0;
}
void intake(float coef ){
	motor[port6] = 127 * coef;
}
void collector(bool in, float time){
	if (in){
		motor[port6] = 127;
	}
	else{
		motor[port6] = 127;
	}
	wait1Msec(time*1000);
}

void move(int displace,float coef){
	SensorValue[right] = 0;
	if(coef < 0){
		displace *= -1;
		while(SensorValue[right]>displace){
			run(coef);
		}
	}
	else{
		while(SensorValue[right]<displace){
			run(coef);
		}
	}
	
	pause();
}

void moveTo(int threshold,float coef){
	SensorValue[right] = 0;
	if(coef < 0){
		while(SensorValue[right]>threshold){
			run(coef);
		}
	}
	else{
		while(SensorValue[right]<threshold){
			run(coef);
		}
	}
	
	pause();
}
