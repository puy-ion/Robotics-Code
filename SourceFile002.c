#pragma config(Motor,  port3,           leftback,      tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port4,           rightfront,    tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port5,           rightback,     tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port6,           intake,        tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port7,           arm,           tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port8,           forearm,       tmotorVex393_MC29, openLoop)
#pragma config(Motor,  port9,           launcher1,     tmotorVex393_MC29, openLoop)
